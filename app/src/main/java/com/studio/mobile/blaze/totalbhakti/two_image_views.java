package com.studio.mobile.blaze.totalbhakti;

public class two_image_views {

    private int ID1;
    private int ID2;
    private String TX1;
    private String TX2;

    public two_image_views(int I1 , int I2 , String t1 , String t2)
      { ID1 = I1; ID2 = I2; TX1 = t1; TX2 = t2;}

    public int getID1() {
        return ID1;
    }
    public int getID2() {
        return ID2;
    }
    public String getTX1() { return TX1; }
    public String getTX2() { return TX2; }

}
